print("Olá, este é um programa para multiplicar dois números sem a operação de multiplicação")
a = int(input("Por favor digite um número: "))
b = int(input("Por favor digite outro número: "))
c = 1
soma = 0
while c<=a:
  soma = soma + b
  c = c+1
print(soma)